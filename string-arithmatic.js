let string1 = "ten";
let string2 = "two";

// Addition (+)
let additionResult = string1 + string2;
console.log("Addition Result:", additionResult); // Output: "tentwo"

// Subtraction (-)
let subtractionResult = string1 - string2;
console.log("Subtraction Result:", subtractionResult); // Output: NaN (Not a Number)

// Multiplication (*)
let multiplicationResult = string1 * string2;
console.log("Multiplication Result:", multiplicationResult); // Output: NaN (Not a Number)

// Division (/)
let divisionResult = string1 / string2;
console.log("Division Result:", divisionResult); // Output: NaN (Not a Number)
