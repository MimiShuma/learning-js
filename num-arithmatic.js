let num1 = "10";
let num2 = "2";

// Addition (+)
// let additionResult = parseInt(num1) + parseInt(num2);
let additionResult = num1 + num2;
console.log("Addition Result:", additionResult); // Output: 12

// Subtraction (-)
// let subtractionResult = parseInt(num1) - parseInt(num2);
let subtractionResult = num1 - num2;
console.log("Subtraction Result:", subtractionResult); // Output: 8

// Multiplication (*)
// let multiplicationResult = parseInt(num1) * parseInt(num2);
let multiplicationResult = num1 * num2;
console.log("Multiplication Result:", multiplicationResult); // Output: 20

// Division (/)
// let divisionResult = parseInt(num1) / parseInt(num2);
let divisionResult = num1 / num2;
console.log("Division Result:", divisionResult); // Output: 5

